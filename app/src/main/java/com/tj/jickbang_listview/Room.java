package com.tj.jickbang_listview;

public class Room {
    public String price;
    public String floor;
    public String adress;
    public String structure;
    public String detail;

    public Room(String price, String floor, String adress, String structure, String detail) {
        this.price = price;
        this.floor = floor;
        this.adress = adress;
        this.structure = structure;
        this.detail = detail;
    }
}
